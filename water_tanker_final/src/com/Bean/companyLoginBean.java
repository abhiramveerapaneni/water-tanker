package com.Bean;

public class companyLoginBean {

	
	private String c_name;
	private String c_password;
	private String c_email;
	private String c_mobile;
	private String c_licence;
	private String c_cost;
	private String c_type;
	private String c_address;
	public companyLoginBean() {
		super();
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_password() {
		return c_password;
	}
	public void setC_password(String c_password) {
		this.c_password = c_password;
	}
	public String getC_email() {
		return c_email;
	}
	public void setC_email(String c_email) {
		this.c_email = c_email;
	}
	public String getC_mobile() {
		return c_mobile;
	}
	public void setC_mobile(String c_mobile) {
		this.c_mobile = c_mobile;
	}
	public String getC_licence() {
		return c_licence;
	}
	public void setC_licence(String c_licence) {
		this.c_licence = c_licence;
	}
	public String getC_cost() {
		return c_cost;
	}
	public void setC_cost(String c_cost) {
		this.c_cost = c_cost;
	}
	public String getC_type() {
		return c_type;
	}
	public void setC_type(String c_type) {
		this.c_type = c_type;
	}
	public String getC_address() {
		return c_address;
	}
	public void setC_address(String c_address) {
		this.c_address = c_address;
	}
	
	
}
